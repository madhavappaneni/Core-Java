class Simple {
    public static void main(String args[]) {
        System.out.println("Hello World");
    }
};

// class - declare a class in Java
// public - access modifier that represents visibility. it meants that the
// method is visible to all
// void - return type of the method. it means it doesn't return any value
// main - represents the starting point of the program
// String args[] or String[] args represents command line arguments

// Other valid main method signatures
// public static void main(String[] args)  
// public static void main(String []args)  
// public static void main(String args[])  
// public static void main(String... args)  
// static public void main(String[] args)  
// public static final void main(String[] args)  
// final public static void main(String[] args)  
// final strictfp public static void main(String[] args)  

// Invalid method signatures
// public void main(String[] args)
// static void main(String[] args)
// public void static main(String[] args)
// abstract public static void main(String[] args)